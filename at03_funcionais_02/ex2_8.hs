{-
Dada uma lista de inteiros com números pares e ímpares, escreva uma função
em Haskell que colocar todos os números pares à frente na lista e os ímpares ao
final. Você não pode usar um gerador de lista para resolver esse problema.
Por exemplo se for informado a lista [3,4,1,2,6,7] para a função a saída ser
[4,2,6,3,1,7].
-}

ordenacaoPorPares :: [Int] -> [Int]
ordenacaoPorPares [] = []
ordenacaoPorPares lista = if mod (head lista) 2 == 0 then (head lista) : ordenacaoPorPares (tail lista) else ordenacaoPorPares (tail lista) ++ [head lista]

main :: IO ()
main = do
  putStrLn "Digite uma lista:"
  lista <- readLn
  print(ordenacaoPorPares lista)
  
